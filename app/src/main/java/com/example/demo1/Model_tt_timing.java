package com.example.demo1;

public class Model_tt_timing {

    String End,Start;

    public Model_tt_timing() {
    }

    public Model_tt_timing(String end, String start) {
        End = end;
        Start = start;
    }

    public String getEnd() {
        return End;
    }

    public void setEnd(String end) {
        End = end;
    }

    public String getStart() {
        return Start;
    }

    public void setStart(String start) {
        Start = start;
    }
}
